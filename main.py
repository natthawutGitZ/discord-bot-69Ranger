def format_names(user_ids, guild):
    """Format user names for display."""
    if not user_ids:
        return None
    names = []
    for uid in user_ids:
        member = guild.get_member(uid)
        if member:
            names.append(f"{member.display_name}")
    return "\n".join(names)
